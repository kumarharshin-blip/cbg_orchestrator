"""
Helper to create encrypted requests for any orchestration endpoint that uses authorization_security middleware.

Usage:
    # Test /process endpoint (default):
    python create_test_request.py
    
    # Test /feedback endpoint:
    python create_test_request.py feedback
    
    # Or use environment variable:
    TEST_MODE=feedback python create_test_request.py
    
Environment Variables:
    Process endpoint:
    - PROCESS_URL: URL of process endpoint (default: http://localhost:8000/api/v1/infiniti/signature-card)
    
    Feedback endpoint:
    - FEEDBACK_URL: URL of feedback endpoint (default: http://localhost:8000/api/v1/feedback)
    - REQ_ID: Request ID for feedback (default: abc123)
    - CASE_ID: Case ID for feedback (default: test_case_456)
    - REMARKS: Feedback remarks (default: Test feedback remarks...)
    - RESPONSE_FEEDBACK: Feedback value - "s" (satisfied) or "f" (feedback/issue) (default: s)
"""
import json
