def create_encrypted_request(
    client_id: str,
    api_key: str,
    encryption_key: str,
    image_path: str,
    metadata: dict = None,
    case_id: str = "test_case_456"
):
    """Build headers and body for an encrypted request. Returns dict with 'headers' and 'body'.
    
    Note: 
    - client_id is sent in headers (handled by auth middleware)
    - req_id is auto-generated by the server
    - parameters, mode, and detection_method are configured via environment variables
    """
    with open(image_path, 'rb') as f:
        file_data_b64 = base64.b64encode(f.read()).decode('utf-8')
    payload = {
        "file_data": file_data_b64,
        "case_id": case_id,
        "metadata": metadata or {}
    }
    cipher = Fernet(encryption_key.encode('utf-8'))
    encrypted_payload = cipher.encrypt(json.dumps(payload).encode('utf-8')).decode('utf-8')
    # Payload encryption done

    # hash : SHA256(api_key|client_id|timestamp|encrypted_payload)
    timestamp = str(int(time.time() * 1000))
    message = f"{api_key}|{client_id}|{timestamp}|{encrypted_payload}"
    hash_signature = hashlib.sha256(message.encode('utf-8')).hexdigest()
    return {
        "headers": {"client_id": client_id, "timestamp": timestamp, "hash": hash_signature, "Content-Type": "application/json"},
        "body": {"encrypted_payload": encrypted_payload}
    }


def create_encrypted_feedback_request(
    client_id: str,
    api_key: str,
    encryption_key: str,
    req_id: str,
    case_id: str,
    remarks: str = None,
    response_feedback: str = None
):
    """Build headers and body for an encrypted feedback request. Returns dict with 'headers' and 'body'.
    
    Note:
    - client_id is sent in headers (handled by auth middleware)
    - req_id, case_id, remarks, response_feedback are in the encrypted payload
    - response_feedback must be "s" (satisfied) or "f" (feedback/issue)
    """
    payload = {
        "req_id": req_id,
        "case_id": case_id,
    }
    if remarks is not None:
        payload["remarks"] = remarks
    if response_feedback is not None:
        payload["response_feedback"] = response_feedback
    
    cipher = Fernet(encryption_key.encode('utf-8'))
    encrypted_payload = cipher.encrypt(json.dumps(payload).encode('utf-8')).decode('utf-8')
    # Payload encryption done

    # hash : SHA256(api_key|client_id|timestamp|encrypted_payload)
    timestamp = str(int(time.time() * 1000))
    message = f"{api_key}|{client_id}|{timestamp}|{encrypted_payload}"
    hash_signature = hashlib.sha256(message.encode('utf-8')).hexdigest()
    return {
        "headers": {"client_id": client_id, "timestamp": timestamp, "hash": hash_signature, "Content-Type": "application/json"},
        "body": {"encrypted_payload": encrypted_payload}
    }


